class Curtain
{
	private String type="windowCurtain";
	private String brand="xyz";
	private String material="Polyester";
	private int price=10;
	private String color="pink";
	Curtain()
	{
	}
	
	void setType(String type)
	{
		this.type=type;
	}
	void setBrand(String brand)
	{
		this.brand=brand;
	}
	void setMaterial(String material)
	{
		this.material=material;
	}
	void setPrice(int price)
	{
		this.price=price;
	}
	void setColor(String color)
	{
		this.color=color;
	}
	
	
	String getType()
	{
		return this.type;
	}
	String getBrand()
	{
		return this.brand;
	}
	String getMaterial()
	{
		return this.material;
	}
	int getPrice()
	{
		return this.price;
	}
	String getColor()
	{
		return this.color;
	}
}
	
	
	