class Developer01
{
	private String name="nnn";
	private String type="ttt";
	private int salary=10000;
	private int experiance=3;
	
	Developer01()
	{
	}
	
	void setName(String name)
	{
		this.name=name;
	}
	void setType(String type)
	{
		this.type=type;
	}
	void setSalary(int salary)
	{
		this.salary=salary;
	}
	void setExperiance(int experiance)
	{
		this.experiance=experiance;
	}
	
	String getName()
	{
		return this.name;
	}
	String getType()
	{
		return this.type;
	}
	int getSalary()
	{
		return this.salary;
	}
	int getExperiance()
	{
		return this.experiance;
	}
}